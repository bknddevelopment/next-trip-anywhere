/**
 * All-Inclusive Resort Database for Newark/Essex County Residents
 *
 * Comprehensive data structure for 100+ all-inclusive resorts targeting
 * 450,000+ monthly searches. Organized by region with detailed information
 * for filtering, comparison, and SEO optimization.
 *
 * Data Requirements:
 * - Each resort must have accurate pricing and flight times
 * - Amenities should be filterable categories
 * - Descriptions must highlight unique selling points
 * - Include both family and adults-only options
 *
 * @see app/all-inclusive-resorts-from-newark/page.tsx for page implementation
 * @see lib/utils/resortFilters.ts for filtering logic
 * @see lib/utils/resortSchema.ts for SEO schema generation
 */

export interface Resort {
  /** Unique identifier */
  id: string
  /** Resort name */
  name: string
  /** Destination (country/region) */
  destination: string
  /** Specific location (city/area) */
  location: string
  /** Resort category */
  category: 'adults-only' | 'family' | 'luxury' | 'budget' | 'party' | 'romantic'
  /** Star rating (3-5) */
  rating: number
  /** Guest review score (0-10) */
  reviewScore: number
  /** Number of reviews */
  reviewCount: number
  /** Price range per person (7 nights from Newark) */
  priceRange: {
    min: number
    max: number
    currency: 'USD'
  }
  /** Flight time from EWR in hours */
  flightTime: number
  /** List of key amenities */
  amenities: string[]
  /** Number of restaurants on property */
  restaurants: number
  /** Number of bars on property */
  bars: number
  /** Number of pools */
  pools: number
  /** Has beach access */
  beachfront: boolean
  /** Brief description (50-100 words) */
  description: string
  /** Unique highlights */
  highlights: string[]
  /** Best for (couples, families, etc.) */
  bestFor: string[]
  /** Room types available */
  roomTypes: string[]
  /** Airport code for nearest airport */
  nearestAirport: string
  /** Transfer time from airport in minutes */
  transferTime: number
  /** Brand/chain affiliation if any */
  brand?: string
  /** Special deals or promotions */
  currentDeals?: string[]
}

export interface ResortRegion {
  /** Region identifier */
  id: string
  /** Display name */
  name: string
  /** SEO-friendly slug */
  slug: string
  /** Average flight time from Newark */
  avgFlightTime: number
  /** Description of region */
  description: string
  /** Best months to visit */
  bestMonths: string[]
  /** Hurricane season notes if applicable */
  weatherNotes?: string
}

