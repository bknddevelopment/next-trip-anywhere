// Helper function to get resorts by category
export function getResortsByCategory(category: Resort['category']): Resort[] {
  return allInclusiveResorts.filter((resort) => resort.category === category)
}

// Helper function to get resorts by destination
export function getResortsByDestination(destination: string): Resort[] {
  return allInclusiveResorts.filter((resort) => resort.destination === destination)
}

// Helper function to get resorts by price range
export function getResortsByPriceRange(minPrice: number, maxPrice: number): Resort[] {
  return allInclusiveResorts.filter(
    (resort) => resort.priceRange.min >= minPrice && resort.priceRange.max <= maxPrice
  )
}

// Helper function to get luxury resorts (5-star or luxury category)
export function getLuxuryResorts(): Resort[] {
  return allInclusiveResorts.filter((resort) => resort.rating === 5 || resort.category === 'luxury')
}

// Helper function to get family-friendly resorts
export function getFamilyResorts(): Resort[] {
  return allInclusiveResorts.filter(
    (resort) => resort.category === 'family' || resort.bestFor.includes('Families')
  )
}

// Helper function to get adults-only resorts
export function getAdultsOnlyResorts(): Resort[] {
  return allInclusiveResorts.filter((resort) => resort.category === 'adults-only')
}

