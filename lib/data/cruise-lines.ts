// Helper functions
export function getCruiseLineBySlug(slug: string): CruiseLine | undefined {
  return CRUISE_LINES.find((line) => line.slug === slug)
}

export function getDealsByCruiseLine(cruiseLineId: string): CruiseDeal[] {
  return CRUISE_DEALS.filter((deal) => deal.cruiseLineId === cruiseLineId)
}

export function getFeaturedDeals(): CruiseDeal[] {
  return CRUISE_DEALS.filter((deal) => deal.featured)
}

export function getLimitedTimeDeals(): CruiseDeal[] {
  return CRUISE_DEALS.filter((deal) => deal.limitedTime)
}
